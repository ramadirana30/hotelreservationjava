    private void srctamuKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_srctamuKeyReleased
        DefaultTableModel model=(DefaultTableModel)tabletamu.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        tabletamu.setRowSorter(tr);
            tr.setRowFilter(RowFilter.regexFilter(srctamu.getText().trim()));
        // TODO add your handling code here:
    }//GEN-LAST:event_srctamuKeyReleased

    private void tabletamuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabletamuMouseClicked
        // TODO add your handling code here:'
        txtid.setText(tabletamu.getValueAt(tabletamu.getSelectedRow(), 0).toString());
        txtnama.setText(tabletamu.getValueAt(tabletamu.getSelectedRow(), 1).toString());
        txtnohp.setText(tabletamu.getValueAt(tabletamu.getSelectedRow(), 2).toString());
        txtemail.setText(tabletamu.getValueAt(tabletamu.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tabletamuMouseClicked

