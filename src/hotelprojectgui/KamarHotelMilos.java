    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tablekamar.getModel();
        int row = tablekamar.getSelectedRow();
        if(row>=0){
            int ok=JOptionPane.showConfirmDialog(null, "Yakin Mau Hapus?","Konfirmasi",JOptionPane.YES_NO_OPTION);
            if(ok==0){
                model.removeRow(row);

            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void hapusdtallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusdtallActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tablekamar.getModel();
        int ok=JOptionPane.showConfirmDialog(null, "Yakin Mau Hapus?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        while (model.getRowCount() > 0){

            for (int i = 0; i < model.getRowCount(); ++i){
                model.removeRow(i); } }
    }//GEN-LAST:event_hapusdtallActionPerformed

